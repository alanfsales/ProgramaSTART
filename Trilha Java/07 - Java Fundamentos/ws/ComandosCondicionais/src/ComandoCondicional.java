
public class ComandoCondicional {

	public static void main(String[] args) {
		//IF
		
		int num1 =10, num2 = 30;
		if (num1 < num2) {
			System.out.println("Número 1 é menor que número 2");
		}
		
		if (num1 > num1) {
			System.out.println("Número 1 é maior que número 2");
		} else {
			System.out.println("Número 1 é menor que número 2");
		}
	}
}
